

/*
    有两种方法，parseInt()和parseFloat
    parseInt()方法将字符串转为整数
    parseFloat方法将字符串转为浮点数
    toFixed()保留几位小数
    
*/
//取整
console.log(parseInt('5.9ssl'))
//取浮点数
console.log(parseFloat('5.9ssl'))
//四舍五入
console.log(Math.round(5.6))
//向上取整
console.log(Math.ceil(5.6))
//向下取整
console.log(Math.floor(5.6))
